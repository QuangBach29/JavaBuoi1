public class ToanTuSoSanh {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        //Toan tu so sanh : >,<,>=,<=,==,/=
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
